CREATE TABLE `sales`.`tb_customer` (
  `CUSTOMER_CODE` VARCHAR(12) NOT NULL COMMENT '고객사코드',
  `CUSTOMER_NAME` VARCHAR(64) NULL COMMENT '고객사명',
  `CUSTOMER_TYPE` VARCHAR(20) NULL COMMENT '고객사구분(거래처,제조사,협력사)',
  `MAIN_OPER_NAME` VARCHAR(20) NULL COMMENT '주담당자',
  `SUB_OPER_NAME` VARCHAR(20) NULL COMMENT '부담당자',
  `MANAGER_TEL_NO` VARCHAR(45) NULL COMMENT '담당자 젆화',
  `MANAGER_EMAIL_ADDR` VARCHAR(45) NULL COMMENT '담당자메일',
  `TEL` VARCHAR(45) NULL COMMENT '전화번호',
  `FAX` VARCHAR(45) NULL COMMENT 'FAX',
  `ADDRESS` VARCHAR(450) NULL COMMENT '주소',
  `ETC`  VARCHAR(500) NULL COMMENT '기타',
  `CEO`  VARCHAR(45) NULL COMMENT '대표이사',
  `COMPANY_TYPE`  VARCHAR(45) NULL COMMENT '기업형태(대기업,관공서,중소기업,기타)',
  `BIZ_TYPE`  VARCHAR(500) NULL COMMENT '업태',
  `BIZ_ITEM`  VARCHAR(500) NULL COMMENT '종목', 
  `USE_YN` VARCHAR(45) NULL COMMENT '사용여부(Y,N)',
  `CREATE_DATE` DATE NULL COMMENT '생성일자',
  `CREATE_ID` VARCHAR(45) NULL COMMENT '생성자',
  `MODIFY_DATE` DATE NULL COMMENT '수정일자',
  `MODIFY_ID` VARCHAR(45) NULL COMMENT '수정자',
  PRIMARY KEY (`CUSTOMER_CODE`))
COMMENT = '고객사';